func ConvertMacRomanToUTF8(macRomanStr string) string {
	var result string
	for _, char := range []byte(macRomanStr) {
		if char < MacRomanStart {
			result += string(char)
		} else {
			result += MacRomanTable[char-MacRomanStart]
		}
	}
	return result
}
